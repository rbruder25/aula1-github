package course;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
      int a = 10; //10+1+1
      int b = ++a; //10+1
      int c = a++; //10+1
      
      System.out.println("valor a:"+a);
      System.out.println("valor b:"+b);
      System.out.println("valor c:"+c);

	}

}
